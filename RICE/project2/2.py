from collections import deque
def bfs_visited(ugraph, start_node):
    'the set of all nodes visited by the algorithm'
    queue=deque()
    visited=[]
    queue.append(start_node)
    while len(queue)!=0:
        step_j=queue.pop()
        if len(ugraph[step_j])!=0:
            for neighbor in ugraph[step_j]:
                if neighbor not in visited:
                    visited.append(neighbor)
                    queue.append(neighbor)
        else:
            visited.append(start_node)
    return  set(visited)

def cc_visited(ugraph):
    'the set of connected components of graph'
    remain_node=ugraph.keys()

    connected_components=[]
    while len(remain_node)!=0:
        begin_node=remain_node[0]
        # print 'begin_node',begin_node
        visited=bfs_visited(ugraph,begin_node)

        connected_components.append(set(visited))
        # print visited,remain_node

        for item in visited:
            # print i,remain_node
            remain_node.remove(item)

    return connected_components

def largest_cc_size(ugraph):
    ' returns the size (an integer) of the largest connected component in ugraph'
    connected_components=cc_visited(ugraph)
    if len(connected_components)==0:
        return 0
    else:
        max_len=len(connected_components[0])
        for item in connected_components:
            if max_len<len(item):
                max_len=len(item)
        return max_len

def compute_resilience(ugraph, attack_order):
    'the function removes the given node and its edges from the graph'
    ret=[]
    ret.append(largest_cc_size(ugraph))

    for attack_node in attack_order:
        if attack_node in ugraph:
            remove_edge=ugraph.pop(attack_node)
            for edge in remove_edge:
                ugraph[edge].remove(attack_node)
            ret.append(largest_cc_size(ugraph))
    return ret
