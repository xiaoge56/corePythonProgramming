def bfs_visited(ugraph, start_node):
    'the set of all nodes visited by the algorithm'
    queue=deque()
    visited=[]
    queue.append(start_node)
    while len(queue)!=0:
        step_j=queue.pop()
        if len(ugraph[step_j])!=0:
            for neighbor in ugraph[step_j]:
                if neighbor not in visited:
                    visited.append(neighbor)
                    queue.append(neighbor)
        else:
            visited.append(start_node)
    return  set(visited)

def cc_visited(ugraph):
    'the set of connected components of graph'
    remain_node=ugraph.keys()

    connected_components=[]
    while len(remain_node)!=0:
        begin_node=remain_node[0]
        # print 'begin_node',begin_node
        visited=bfs_visited(ugraph,begin_node)

        connected_components.append(set(visited))
        # print visited,remain_node

        for item in visited:
            # print i,remain_node
            remain_node.remove(item)

    return connected_components

